// 5. *С помощью рекурсии организовать функцию возведения числа в степень. Формат: function power($val, $pow), где $val – заданное число, $pow – степень.

function power($val, $pow)
{
    if ($val == 0) {
        return 0;
    }
    if ($val == 1) {
        return 1;
    }
    if ($pow == 1) {
        return $val;
    }
    return $val * power($val, $pow - 1);
}

echo power(0, 3) . "\n";
echo power(1, 3) . "\n";
echo power(5, 1) . "\n";
echo power(3, 3) . "\n";

// 6. *Написать функцию, которая вычисляет текущее время и возвращает его в формате с правильными склонениями, например:
// 22 часа 15 минут
// 21 час 43 минуты.

function getWord($number, $one, $two, $five)
{
    $n = $number % 100;
    $n1 = $n % 10;
    if ($n1 == 1) {
        return $one;
    }
    if ($n1 > 1 && $n1 < 5) {
        return $two;
    }
    if ($n > 10 && $n < 20) {
        return $five;
    }


    return $five;
}

function getCurrentTime()
{
    $hours = date('G');
    $minutes = date('i');
    $hours_word = getWord($hours, "час", "часа", "часов");
    $minutes_word = getWord($minutes, "минута", "минуты", "минут");
    return "$hours $hours_word $minutes $minutes_word";
}

echo getCurrentTime();
