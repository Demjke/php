function calc($a, $b, $op)
{
    switch ($op) {
        case '+':
            return $a . ' + ' . $b . ' = ' . $a + $b;
            break;
        case '-':
            return $a . ' - ' . $b . ' = ' . $a - $b;
            break;
        case '*':
            return $a . ' * ' . $b . ' = ' . $a * $b;
            break;
        case '/':
            return $a . ' / ' . $b . ' = ' . $a / $b;
            break;
        default:
            echo "Неверные значения";
    }
}
